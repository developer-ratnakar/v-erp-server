CREATE TABLE IF NOT EXISTS exam_results (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  exam_id BIGINT NOT NULL REFERENCES academic_exams(id) ON DELETE CASCADE,
  student_id BIGINT NOT NULL REFERENCES students(id) ON DELETE CASCADE,
  total_marks DOUBLE PRECISION NOT NULL DEFAULT 0.0,
  secured_marks DOUBLE PRECISION NOT NULL DEFAULT 0.0,
  percentage DOUBLE PRECISION NOT NULL DEFAULT 0.0,
  sgpa DOUBLE PRECISION,
  cgpa DOUBLE PRECISION,
  result_status VARCHAR(20) NOT NULL DEFAULT 'PASS',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS idx_exam_results_exam_id ON exam_results(exam_id);
CREATE INDEX IF NOT EXISTS idx_exam_results_student_id ON exam_results(student_id);
CREATE TRIGGER update_exam_results_updated_at
BEFORE UPDATE ON exam_results
FOR EACH ROW EXECUTE PROCEDURE update_updated_at_column();

CREATE TABLE IF NOT EXISTS exam_marks (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  exam_id BIGINT NOT NULL REFERENCES academic_exams(id) ON DELETE CASCADE,
  student_id BIGINT NOT NULL REFERENCES students(id) ON DELETE CASCADE,
  subject_id BIGINT NOT NULL REFERENCES operations_subjects(id) ON DELETE RESTRICT,
  result_id BIGINT REFERENCES exam_results(id) ON DELETE CASCADE,
