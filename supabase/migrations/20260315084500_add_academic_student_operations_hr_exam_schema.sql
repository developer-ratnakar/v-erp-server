-- Lookup enums
DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'blood_group') THEN
    CREATE TYPE blood_group AS ENUM ('A+', 'A-', 'B+', 'B-', 'AB+', 'AB-', 'O+', 'O-', 'UNKNOWN');
  END IF;
END $$;

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'admission_type') THEN
    CREATE TYPE admission_type AS ENUM ('HOSTELLER', 'DAY_SCHOLAR');
  END IF;
END $$;

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'address_type') THEN
    CREATE TYPE address_type AS ENUM ('PERMANENT', 'CURRENT');
  END IF;
END $$;

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'student_document_type') THEN
    CREATE TYPE student_document_type AS ENUM (
      '10TH_MARKSHEET',
      '12TH_MARKSHEET',
      'DIPLOMA_CERTIFICATE',
      'JEE_MARKSHEET',
      'OJEE_MARKSHEET',
      'INCOME_CERTIFICATE',
      'CASTE_CERTIFICATE',
      'RESIDENCE_CERTIFICATE',
      'OTHER'
    );
  END IF;
END $$;

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'exam_status') THEN
    CREATE TYPE exam_status AS ENUM ('SCHEDULED', 'ONGOING', 'COMPLETED', 'PUBLISHED');
  END IF;
END $$;

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'weekday_code') THEN
    CREATE TYPE weekday_code AS ENUM ('MON', 'TUE', 'WED', 'THU', 'FRI', 'SAT', 'SUN');
  END IF;
END $$;

-- Academic
CREATE TABLE IF NOT EXISTS academic_programs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code VARCHAR(20) NOT NULL UNIQUE,
  name VARCHAR(100) NOT NULL,
  is_active BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS idx_academic_programs_code ON academic_programs(code);
CREATE TRIGGER update_academic_programs_updated_at
BEFORE UPDATE ON academic_programs
FOR EACH ROW EXECUTE PROCEDURE update_updated_at_column();

CREATE TABLE IF NOT EXISTS academic_departments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
