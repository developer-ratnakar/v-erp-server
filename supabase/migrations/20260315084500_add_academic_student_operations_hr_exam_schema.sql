  remarks TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ,
  CONSTRAINT chk_exam_marks_total_marks CHECK (total_marks >= 0),
  CONSTRAINT chk_exam_marks_marks_obtained CHECK (marks_obtained >= 0)
);

CREATE INDEX IF NOT EXISTS idx_exam_marks_exam_id ON exam_marks(exam_id);
CREATE INDEX IF NOT EXISTS idx_exam_marks_student_id ON exam_marks(student_id);
CREATE INDEX IF NOT EXISTS idx_exam_marks_subject_id ON exam_marks(subject_id);
CREATE INDEX IF NOT EXISTS idx_exam_marks_result_id ON exam_marks(result_id);
CREATE TRIGGER update_exam_marks_updated_at
BEFORE UPDATE ON exam_marks
FOR EACH ROW EXECUTE PROCEDURE update_updated_at_column();
