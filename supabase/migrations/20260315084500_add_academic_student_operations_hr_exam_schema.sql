-- Students
CREATE TABLE IF NOT EXISTS students (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  registration_number VARCHAR(100) UNIQUE,
  apaar_id VARCHAR(100) UNIQUE,
  first_name VARCHAR(100) NOT NULL,
  middle_name VARCHAR(100),
  last_name VARCHAR(100) NOT NULL,
  dob DATE NOT NULL,
  photo_url TEXT,
  email VARCHAR(255),
  phone_number VARCHAR(20),
  blood_group blood_group DEFAULT 'UNKNOWN',
  admission_type admission_type NOT NULL DEFAULT 'DAY_SCHOLAR',
  program_id BIGINT REFERENCES academic_programs(id) ON DELETE SET NULL,
  department_id BIGINT REFERENCES academic_departments(id) ON DELETE SET NULL,
  batch_id BIGINT REFERENCES academic_batches(id) ON DELETE SET NULL,
  semester_id BIGINT REFERENCES academic_semesters(id) ON DELETE SET NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS idx_students_registration_number ON students(registration_number);
CREATE INDEX IF NOT EXISTS idx_students_apaar_id ON students(apaar_id);
CREATE INDEX IF NOT EXISTS idx_students_program_id ON students(program_id);
CREATE INDEX IF NOT EXISTS idx_students_department_id ON students(department_id);
CREATE INDEX IF NOT EXISTS idx_students_batch_id ON students(batch_id);
CREATE INDEX IF NOT EXISTS idx_students_semester_id ON students(semester_id);
CREATE TRIGGER update_students_updated_at
BEFORE UPDATE ON students
FOR EACH ROW EXECUTE PROCEDURE update_updated_at_column();

