CREATE TABLE IF NOT EXISTS operations_timetable_entries (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  timetable_id BIGINT NOT NULL REFERENCES operations_timetables(id) ON DELETE CASCADE,
  day weekday_code NOT NULL,
  start_time TIME NOT NULL,
  end_time TIME NOT NULL,
  subject_id BIGINT REFERENCES operations_subjects(id) ON DELETE SET NULL,
  is_break BOOLEAN NOT NULL DEFAULT FALSE,
  break_name VARCHAR(50),
  room_no VARCHAR(20),
  faculty_id BIGINT REFERENCES hr_staff(id) ON DELETE SET NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ,
  CONSTRAINT chk_operations_timetable_entries_time_range CHECK (end_time > start_time),
  CONSTRAINT chk_operations_timetable_entries_break_name CHECK (
    (is_break = TRUE AND break_name IS NOT NULL) OR
    (is_break = FALSE)
  )
);

CREATE INDEX IF NOT EXISTS idx_operations_timetable_entries_timetable_id ON operations_timetable_entries(timetable_id);
CREATE INDEX IF NOT EXISTS idx_operations_timetable_entries_subject_id ON operations_timetable_entries(subject_id);
CREATE TRIGGER update_operations_timetable_entries_updated_at
BEFORE UPDATE ON operations_timetable_entries
FOR EACH ROW EXECUTE PROCEDURE update_updated_at_column();

CREATE TABLE IF NOT EXISTS operations_attendance (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  student_id BIGINT NOT NULL REFERENCES students(id) ON DELETE CASCADE,
  subject_id BIGINT NOT NULL REFERENCES operations_subjects(id) ON DELETE CASCADE,
  month DATE NOT NULL,
  attendance_data TEXT NOT NULL DEFAULT '',
  program_id BIGINT REFERENCES academic_programs(id) ON DELETE SET NULL,
  department_id BIGINT REFERENCES academic_departments(id) ON DELETE SET NULL,
  batch_id BIGINT REFERENCES academic_batches(id) ON DELETE SET NULL,
  semester_id BIGINT REFERENCES academic_semesters(id) ON DELETE SET NULL,
  session_id BIGINT REFERENCES academic_sessions(id) ON DELETE SET NULL,
  CONSTRAINT uq_student_subject_month UNIQUE (student_id, subject_id, month)
);

CREATE INDEX IF NOT EXISTS idx_operations_attendance_student_id ON operations_attendance(student_id);
CREATE INDEX IF NOT EXISTS idx_operations_attendance_subject_id ON operations_attendance(subject_id);
CREATE INDEX IF NOT EXISTS idx_operations_attendance_month ON operations_attendance(month);

-- Exams
CREATE TABLE IF NOT EXISTS academic_exams (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  session_id BIGINT NOT NULL REFERENCES academic_sessions(id) ON DELETE RESTRICT,
  program_id BIGINT REFERENCES academic_programs(id) ON DELETE SET NULL,
  batch_id BIGINT REFERENCES academic_batches(id) ON DELETE SET NULL,
  semester_id BIGINT NOT NULL REFERENCES academic_semesters(id) ON DELETE RESTRICT,
  start_date DATE NOT NULL,
  end_date DATE NOT NULL,
  status exam_status NOT NULL DEFAULT 'SCHEDULED',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ,
  CONSTRAINT chk_academic_exams_date_range CHECK (end_date >= start_date)
);

CREATE INDEX IF NOT EXISTS idx_academic_exams_session_id ON academic_exams(session_id);
